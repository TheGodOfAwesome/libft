#include "libft.h"

void	ft_putstrs(char **av, int ac)
{
	int		i;

	i = 0;
	while (i < ac)
	{
		if (av[i])
		{
			ft_putstr(av[i]);
			ft_putchar('\n');
		}
		i++;
	}
}
